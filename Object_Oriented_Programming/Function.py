# Object_Oriented_Programming
class function_name:

    #Without arguments
    def function(self):
        return "This is a function code"
    

    #With Aarguments
    def function_arg(self,Name,age):
        return "My name is %s and age is %d"%(Name,age)
   
   

    #Sum 
    def sum(self,a,b):
        return a+b
    
   
d1 = function_name()
print(d1.function())
